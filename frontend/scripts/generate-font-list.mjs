import { readdirSync } from "fs";
import { join, basename } from "path";
import { readFileSync, writeFileSync } from "fs";

const ESM_DIR = join(
  process.cwd(),
  "node_modules/@remotion/google-fonts/dist/esm",
);

const files = readdirSync(ESM_DIR).filter((f) => f.endsWith(".mjs"));

const fonts = [];

for (const file of files) {
  const content = readFileSync(join(ESM_DIR, file), "utf-8");
  const match = content.match(/fontFamily\s*=\s*"([^"]+)"/);
  if (match) {
    const moduleName = basename(file, ".mjs");
    fonts.push({ module: moduleName, name: match[1] });
  }
}

fonts.sort((a, b) => a.name.localeCompare(b.name));

const output = [
  "// Auto-generated by scripts/generate-font-list.mjs — DO NOT EDIT",
  "export const ALL_FONTS: { module: string; name: string }[] = [",
  ...fonts.map((f) => `  { module: "${f.module}", name: "${f.name}" },`),
  "];",
  "",
].join("\n");

writeFileSync(join(process.cwd(), "src/lib/font-list.ts"), output);
console.log(`Generated ${fonts.length} fonts`);
